package class4;

public class Task5_EvenOrOddNumber {

    /*
    Interview question
    Write a program to check whether number is Even or Odd
     */
    public static void main(String[] args) {
        int number = -5;

        if (number%2 == 0) {
            System.out.println("Number is Even");
        } else {
            System.out.println("Number is Odd");
        }
    }
}
